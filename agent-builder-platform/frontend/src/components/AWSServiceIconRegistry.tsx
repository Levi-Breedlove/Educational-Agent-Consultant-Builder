/**
 * Get service definition by ID
 */
export function getService(id: string): AWSServiceDefinition | undefined {
  return awsServiceRegistry[id]
}

/**
 * Get all services in a category
 */
export function getServicesByCategory(category: string): AWSServiceDefinition[] {
  const serviceIds = servicesByCategory[category] || []
  return serviceIds.map(id => awsServiceRegistry[id]).filter(Boolean)
}

/**
 * Search services by name, description, or tags
 */
export function searchServices(query: string): AWSServiceDefinition[] {
  const lowerQuery = query.toLowerCase()
  return Object.values(awsServiceRegistry).filter(service =>
    service.name.toLowerCase().includes(lowerQuery) ||
    service.officialName.toLowerCase().includes(lowerQuery) ||
    service.description.toLowerCase().includes(lowerQuery) ||
    service.tags.some(tag => tag.toLowerCase().includes(lowerQuery)) ||
    service.useCases.some(useCase => useCase.toLowerCase().includes(lowerQuery))
  )
}

/**
 * Get all categories
 */
export function getAllCategories(): string[] {
  return Object.keys(servicesByCategory)
}

/**
 * Get total service count
 */
export function getTotalServiceCount(): number {
  return Object.keys(awsServiceRegistry).length
}

